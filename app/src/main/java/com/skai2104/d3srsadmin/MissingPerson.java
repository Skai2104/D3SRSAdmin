package com.skai2104.d3srsadmin;

public class MissingPerson {
    private String mName, mAge, mGender, mLocation, mAttire, mHeight, mWeight, mAddress1, mAddress2,
            mFacial, mPhysical, mBody, mHabits, mAdditional, mPhone, mEmail, mStatus, mReported, mDocId;

    public MissingPerson() {
    }

    public MissingPerson(String name, String age, String gender, String location, String attire, String height, String weight, String address1, String address2, String facial, String physical, String body, String habits, String additional, String phone, String email, String status, String reported) {
        mName = name;
        mAge = age;
        mGender = gender;
        mLocation = location;
        mAttire = attire;
        mHeight = height;
        mWeight = weight;
        mAddress1 = address1;
        mAddress2 = address2;
        mFacial = facial;
        mPhysical = physical;
        mBody = body;
        mHabits = habits;
        mAdditional = additional;
        mPhone = phone;
        mEmail = email;
        mStatus = status;
        mReported = reported;
    }

    public String getName() {
        return mName;
    }

    public String getAge() {
        return mAge;
    }

    public String getGender() {
        return mGender;
    }

    public String getLocation() {
        return mLocation;
    }

    public String getAttire() {
        return mAttire;
    }

    public String getHeight() {
        return mHeight;
    }

    public String getWeight() {
        return mWeight;
    }

    public String getAddress1() {
        return mAddress1;
    }

    public String getAddress2() {
        return mAddress2;
    }

    public String getFacial() {
        return mFacial;
    }

    public String getPhysical() {
        return mPhysical;
    }

    public String getBody() {
        return mBody;
    }

    public String getHabits() {
        return mHabits;
    }

    public String getAdditional() {
        return mAdditional;
    }

    public String getPhone() {
        return mPhone;
    }

    public String getEmail() {
        return mEmail;
    }

    public String getStatus() {
        return mStatus;
    }

    public String getReported() {
        return mReported;
    }

    public String getDocId() {
        return mDocId;
    }

    public void setName(String name) {
        mName = name;
    }

    public void setAge(String age) {
        mAge = age;
    }

    public void setGender(String gender) {
        mGender = gender;
    }

    public void setLocation(String location) {
        mLocation = location;
    }

    public void setAttire(String attire) {
        mAttire = attire;
    }

    public void setHeight(String height) {
        mHeight = height;
    }

    public void setWeight(String weight) {
        mWeight = weight;
    }

    public void setAddress1(String address1) {
        mAddress1 = address1;
    }

    public void setAddress2(String address2) {
        mAddress2 = address2;
    }

    public void setFacial(String facial) {
        mFacial = facial;
    }

    public void setPhysical(String physical) {
        mPhysical = physical;
    }

    public void setBody(String body) {
        mBody = body;
    }

    public void setHabits(String habits) {
        mHabits = habits;
    }

    public void setAdditional(String additional) {
        mAdditional = additional;
    }

    public void setPhone(String phone) {
        mPhone = phone;
    }

    public void setEmail(String email) {
        mEmail = email;
    }

    public void setStatus(String status) {
        mStatus = status;
    }

    public void setReported(String reported) {
        mReported = reported;
    }

    public void setDocId(String docId) {
        mDocId = docId;
    }
}
